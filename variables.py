# Data types
myStr = "String"
myInt = 25
myFloat = 1.3

myList = [1, 2, 3, "First"]
myDictionary = {"a": 1, "b": 2, "c": 3}

print(type(myStr), myStr)
print(type(myInt), myInt)
print(type(myFloat), myFloat)
print(type(myList), myList)
print(type(myDictionary), myDictionary)

print(myList[3])
print(myDictionary["a"])

print(myStr, "World")